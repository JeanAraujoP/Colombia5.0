//  TABS DE CONTENIDO (Español / Inglés dentro de conf-card y conclusión)
// ══════════════════════════════════════
document.querySelectorAll(".content-tab").forEach(tab => {
  tab.addEventListener("click", function () {
    const target = this.getAttribute("data-target");

    // Buscar el contenedor padre más cercano que tenga tabs y lang-content
    const section = this.closest(".conf-body") || this.closest("section") || this.closest(".section-inner") || document;

    // Desactivar tabs del mismo grupo
    const tabGroup = this.closest(".content-tabs");
    tabGroup.querySelectorAll(".content-tab").forEach(t => t.classList.remove("active"));
    this.classList.add("active");

    // Ocultar todos los lang-content del mismo padre
    // Buscamos el padre que contiene los lang-content
    let parent = tabGroup.parentElement;
    parent.querySelectorAll(".lang-content").forEach(content => {
      content.classList.remove("active");
    });

    // Mostrar el target
    const targetEl = parent.querySelector(`[data-lang="${target}"]`);
    if (targetEl) targetEl.classList.add("active");
  });
});

