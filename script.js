//  BUSCADOR DEL GLOSARIO
// ══════════════════════════════════════
function filterGlossary() {
  const input = document.getElementById("glossSearch");
  if (!input) return;

  const query = input.value.toLowerCase().trim();
  const rows = document.querySelectorAll(".glossary-row");

  rows.forEach(row => {
    const text = row.textContent.toLowerCase();
    if (query === "" || text.includes(query)) {
      row.style.display = "";
    } else {
      row.style.display = "none";
    }
  });

  // Mostrar mensaje si no hay resultados
  let noResults = document.getElementById("gloss-no-results");
  const tbody = document.getElementById("glossary-body");
  const visible = [...rows].filter(r => r.style.display !== "none");

  if (visible.length === 0) {
    if (!noResults) {
      noResults = document.createElement("tr");
      noResults.id = "gloss-no-results";
      noResults.innerHTML = `<td colspan="3" style="text-align:center; padding:2rem; color: var(--morado); font-style:italic;">
        ${currentLang === "es" ? "No se encontraron términos." : "No terms found."}
      </td>`;
      tbody.appendChild(noResults);
    } else {
      noResults.style.display = "";
      noResults.querySelector("td").textContent =
        currentLang === "es" ? "No se encontraron términos." : "No terms found.";
    }
  } else {
    if (noResults) noResults.style.display = "none";
  }
}

